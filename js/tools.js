function leapMonth(y){ return lunarInfo[y-1900] & 0xf; }

function leapDays(y){
    if(leapMonth(y)){
        return (lunarInfo[y-1900] & 0x10000) ? 30 : 29;
    }
    return 0;
}

function monthDays(y,m){
    return (lunarInfo[y-1900] & (0x10000>>m)) ? 30 : 29;
}

function lYearDays(y){
    let sum = 348;
    for(let i=0x8000;i>0x8;i>>=1){
        sum += (lunarInfo[y-1900] & i) ? 1 : 0;
    }
    return sum + leapDays(y);
}

function solarToLunar(date){

    let offset = (Date.UTC(date.getFullYear(),date.getMonth(),date.getDate())
        - Date.UTC(1900,0,31)) / 86400000;

    let lunarYear,lunarMonth,lunarDay;
    let temp=0;

    for(lunarYear=1900;lunarYear<2101 && offset>0;lunarYear++){
        temp=lYearDays(lunarYear);
        if(offset < temp) break;
        offset-=temp;
    }

    let leap=leapMonth(lunarYear);
    let isLeap=false;

    for(lunarMonth=1;lunarMonth<=12;lunarMonth++){

        if(leap>0 && lunarMonth==(leap+1) && !isLeap){
            --lunarMonth;
            isLeap=true;
            temp=leapDays(lunarYear);
        }else{
            temp=monthDays(lunarYear,lunarMonth);
        }

        if(offset < temp) break;

        offset -= temp;

        if(isLeap && lunarMonth==(leap+1)){
            isLeap=false;
        }
    }

    lunarDay = offset + 1;

    let monthStr = (isLeap ? "闰" : "") + chineseMonth[lunarMonth-1];
    let dayStr = chineseDay[lunarDay-1];

    return lunarYear + "年" + monthStr + dayStr;
}

